-- ========================================================
-- STEP 4 -- insert responses (three-state answer)
-- ========================================================
-- CAST('{...}' AS JSON) stores a real JSON boolean/null, which is what the
-- PHP check (answer === true) needs. Do NOT use JSON_OBJECT with TRUE/FALSE
-- -- that stores 1/0 and breaks has_conflict.
--     Yes   -> true    No -> false    blank -> null

INSERT INTO `coi_responses`
    (`coi_declaration_id`, `question_key`, `response_value`, `created_at`, `updated_at`)
SELECT
    d.`id`,
    '2025_has_conflict',
    CASE
        WHEN LOWER(TRIM(i.`deklarasi`)) = 'yes'
            THEN CAST('{"answer": true,  "details": [], "attachment": null}' AS JSON)
        WHEN LOWER(TRIM(i.`deklarasi`)) = 'no'
            THEN CAST('{"answer": false, "details": [], "attachment": null}' AS JSON)
        ELSE CAST('{"answer": null,  "details": [], "attachment": null}' AS JSON)
    END,
    NOW(), NOW()
FROM `coi_declarations` d
JOIN `hcispanel_hc`.`employees` e ON e.`id` = d.`user_id`
JOIN `coi_2025_import` i
    ON i.`employee_id` = e.`employee_id` COLLATE utf8mb4_unicode_ci
WHERE d.`period` = 2025 AND d.`type` = 'employee'
  AND NOT EXISTS (
      SELECT 1 FROM `coi_responses` r
      WHERE r.`coi_declaration_id` = d.`id` AND r.`question_key` = '2025_has_conflict'
  );


